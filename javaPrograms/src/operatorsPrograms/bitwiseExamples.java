package operatorsPrograms;

public class bitwiseExamples {

	public static void main(String[] args) {
				
		int num1 = 5;
		int num2 = 6;
		
		System.out.println("(num1&num2) = "+(num1&num2)); //4
		
		System.out.println("(num1|num2) = "+(num1|num2));  // 7
		
		System.out.println("(~num1)  = "+(~num1)); // 10 or -6
		
		System.out.println("(num1^num2) = "+(num1^num2)); // 3
		
		System.out.println("(num1<<2) = "+(num1<<2)); // 20
		
		System.out.println("(num1>>2) = "+(num1>>2)); // 1
		
		
	}

}
