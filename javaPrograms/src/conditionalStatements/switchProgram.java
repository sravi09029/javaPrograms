package conditionalStatements;

public class switchProgram {

	public static void main(String[] args) {
			
		 int number = 100;
		 
		 switch(number) {
		 
		 case 20: System.out.println("number is a 20");
		 break;
		 case 30: System.out.println("number is a 30");
		 break;
		 case 50: System.out.println("number is a 50");
		 break;
		 case 100: System.out.println("number is a 100");
		 break;
		 case 80: System.out.println("number is a 80");
		 break;
		 case 70: System.out.println("number is a 70");
		 break;
		 
		 default: System.out.println("none of the  above");
		 }
	}

}
